void	move_buf(char *buf)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (buf[i] && buf[i] != '\n')
	{
		buf[i] = 0;
		i++;
	}
	if (buf[i] == '\n')
	{
		buf[i] = 0;
		i++;
		while (buf[i])
		{
			buf[j] = buf[i];
			buf[i] = 0;
			j++;
			i++;
		}
	}
}
