#include "libft.h"

int ft_memcmp(const void *s1, const void *s2, size_t n)
{
    int i;

    i = 0;
    if (n == '0')
        return ('0');
    while (*s1 == *s2 && i <= n)
    {
        i++;
        s1++;
        s2++;
    }
    return (*s1 - *s2);
}
